#!/usr/bin/env node
// Local test-signing: generate ephemeral key, overwrite pubkey bake, sign manifest.
// USE ONLY FOR LOCAL SMOKE — the generated key is discarded after the session.
import {writeFileSync, readFileSync} from 'fs';
import * as ed from '@noble/ed25519';
import {sha512} from '@noble/hashes/sha2.js';

ed.hashes.sha512 = sha512;

const priv = ed.utils.randomSecretKey();
const pub = await ed.getPublicKeyAsync(priv);
const pubB64 = Buffer.from(pub).toString('base64');
const fingerprint = 'ed25519:' + Buffer.from(pub).toString('hex').slice(0, 16);

// 1. Overwrite trusted-pubkey.generated.ts
const pubTs = `/* eslint-disable */
// TEST-ONLY ephemeral key — regenerated by /tmp/test-sign.mjs
// Do NOT commit this state. Restore from backup before git operations.
export const TRUSTED_PUBKEY_B64 = '${pubB64}';
export const TRUSTED_PUBKEY_FINGERPRINT = '${fingerprint}';
`;
writeFileSync('src/lib/update/signing/trusted-pubkey.generated.ts', pubTs);

// 2. Save private key temporarily for signing after rebuild
writeFileSync('/tmp/test-priv.b64', Buffer.from(priv).toString('base64'));

console.log('Test pubkey baked:', fingerprint);
console.log('Private key saved to /tmp/test-priv.b64');
console.log('Next: pnpm build && node /tmp/test-sign-manifest.mjs');
